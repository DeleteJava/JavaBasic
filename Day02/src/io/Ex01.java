package io;				/* package : 클래스 파일을 묶어서 보관하는 개체  */

public class Ex01 {		// class : .Java 파일이 컴파일된 후의 개체
	
	public static void main(String[] args) {
		// main 함수 : 코드의 시작점
		
		System.out.println("Hello World!!!");
		// - System 클래스 : 표준 입출력을 처리하는 클래스
		// - out 객체 : 출력 객체
		// - println() 함수 : 화면에 '문자열'을 출력하는 기능
		
		System.out.println("안녕 세상아!!!");
		
		System.out.println("코드는 위에서 아래로 순차적으로 수행~");
		
	}
}
 